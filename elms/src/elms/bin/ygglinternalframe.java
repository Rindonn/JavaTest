    public void refresh(List<Employee> list){
       DefaultTableModel model = (DefaultTableModel) this.tabemployee.getModel();
     //删除表格中的行
     while(model.getRowCount()>0){
         model.removeRow(0);
     }
     //便利集合
     for(Employee p : list){
         //将数据放到一个集合里 然后在放到表格中
        Vector v = new Vector();
        v.add(p.getUid());
        v.add(p.getUsername());
        v.add(p.getPassword());
        v.add(p.getTruename());
        v.add(p.getPosition());
        v.add(p.getPhone());
        v.add(p.getCid());
        //放到表格里
        model.addRow(v);
    }
    }

