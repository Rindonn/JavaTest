    public void refresh(List<Purchase> list){
        DefaultTableModel model = (DefaultTableModel) this.tabcaigou.getModel();
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        for(Purchase p:list){
            Vector v = new Vector();
            v.add(p.getPurid());
            v.add(p.getProid());
            v.add(p.getProname());
            v.add(p.getProtype());
            v.add(p.getPurprice());
            v.add(p.getSupfullname());
            v.add(p.getPurdate());
            v.add(p.getPurnumber());
            v.add(p.getReturndate());
            v.add(p.getReturnamount());
            v.add(p.getReturnreason());
            model.addRow(v);
        }
        
    }
