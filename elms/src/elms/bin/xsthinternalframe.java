     public void refresh(List<Sell> list){
        DefaultTableModel model = (DefaultTableModel) this.tabxiaoshou.getModel();
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        for(Sell p:list){
            Vector v = new Vector();
            v.add(p.getSeid());
            v.add(p.getProid());
            v.add(p.getProname());
            v.add(p.getSellprice());
            v.add(p.getSellnum());
            v.add(p.getCusname());
            v.add(p.getSelldate());
            v.add(p.getTruename());
            v.add(p.getSellreturnamount());
            v.add(p.getSellreturndate());
            model.addRow(v);
        }
        
    }
