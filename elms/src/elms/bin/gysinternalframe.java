    public void refresh(List<Supplier> list){
       DefaultTableModel model = (DefaultTableModel) this.tabSupplier.getModel();
     //删除表格中的行
     while(model.getRowCount()>0){
         model.removeRow(0);
     }
        //便利集合
        for (Supplier p : list) {
            //将数据放到一个集合里 然后在放到表格中
            Vector v = new Vector();
            v.add(p.getSupid());
            v.add(p.getSupaddreviation());
            v.add(p.getSupfullname());
            v.add(p.getOwner());
            v.add(p.getJob());
            v.add(p.getMobile());
            v.add(p.getSupaddress());
            //放到表格里
            model.addRow(v);
        }
   }

